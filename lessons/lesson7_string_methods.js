// string methods and attributes

let userName = "Cirus D Punk";

console.log("userName string length: ", userName.length);
console.log("Getting the first character from userName: ", userName.charAt(0));
console.log("Getting the position of any letter on userName: ", userName.indexOf("i"));

console.log("userName in UpperCase: ", userName.toUpperCase())
console.log("userName in LowerCase: ", userName.toLowerCase())

